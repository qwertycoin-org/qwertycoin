#pragma once

#include <time.h>

namespace CryptoNote {

struct ITimeProvider
{
    virtual ~ITimeProvider() = default;

    virtual time_t now() = 0;
};

struct RealTimeProvider : public ITimeProvider
{
    time_t now() override
    {
        return time(nullptr);
    }
};

} // namespace CryptoNote
